    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        btnEditar.addActionListener(e -> {
            Contato dao = new Contato();
            int linhaSelecionada = tabelaContatos.getSelectedRow();
            if (linhaSelecionada >= 0) {
                int id = Integer.parseInt(tabelaContatos.getValueAt(linhaSelecionada, 0).toString());
                String nome = txtNome.getText();
                String telefone = txtTelefone.getText();
                String email = txtEmail.getText();

                dao.editarContato(id, nome, telefone, email);
                dao.listarContatos((DefaultTableModel) tabelaContatos.getModel());
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um contato da tabela para editar.");
            }
        });

        tabelaContatos.getSelectionModel().addListSelectionListener(event -> {
            int linha = tabelaContatos.getSelectedRow();
            if (linha >= 0) {
                txtNome.setText(tabelaContatos.getValueAt(linha, 1).toString());
                txtTelefone.setText(tabelaContatos.getValueAt(linha, 2).toString());
                txtEmail.setText(tabelaContatos.getValueAt(linha, 3).toString());
            }
        });


    }//GEN-LAST:event_btnEditarActionPerformed

