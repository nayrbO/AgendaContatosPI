    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        btnExcluir.addActionListener(e -> {
            Contato dao = new Contato();
            int linhaSelecionada = tabelaContatos.getSelectedRow();
            if (linhaSelecionada >= 0) {
                int id = Integer.parseInt(tabelaContatos.getValueAt(linhaSelecionada, 0).toString());
                int confirmacao = JOptionPane.showConfirmDialog(
                        null,
                        "Deseja realmente excluir este contato?",
                        "Confirmação",
                        JOptionPane.YES_NO_OPTION
                );

                if (confirmacao == JOptionPane.YES_OPTION) {
                    dao.excluirContato(id);
                    dao.listarContatos((DefaultTableModel) tabelaContatos.getModel()); // atualiza a tabela
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um contato para excluir.");
            }
        });


    }//GEN-LAST:event_btnExcluirActionPerformed

